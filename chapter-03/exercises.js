////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, char) {
  let count = 0; // Initialize a counter
  for (let i = 0; i < str.length; i++) {
      if (str[i] === char) {
          count++; // Increment the counter if the character matches
      }
  }
  return count; // Return the final count
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(str) {
  let count = 0;
  for (let i = 0; i < str.length; i++) {
    if (str[i] === 'B') {
      count++;
    }
  }
  return count;
}

